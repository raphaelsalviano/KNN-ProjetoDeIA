// Classe de imagem
public class Imagem {
	
	private int mRotulo;
	private int[] mMatriz;
	
	public Imagem(int mRotulo, int[] mMatriz) {
		super();
		this.mRotulo = mRotulo;
		this.mMatriz = mMatriz;
	}

	public int getmRotulo() {
		return mRotulo;
	}

	public int[] getmMatriz() {
		return mMatriz;
	}
	
}
